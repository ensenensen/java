package day10;

public class Animal {
	private String name;
	private String sound;
	private int leg;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	// 모든필드 set/get메서드 만들기
	public void setSound(String sound) {
		this.sound = sound;
	}
	public String getSound() {
		return sound;
	}
	public void setLeg(int leg) {
		this.leg = leg;
	}
	public int getLeg() {
		return leg;
	}
	
}






